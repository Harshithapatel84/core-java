class ShortSwitch{
    public static void main(String[] args)
	{
		short houseNumber=23;
		switch (houseNumber)
		{
			case 14:
			System.out.println("house no is 14");
			break;
			
			case 65:
			System.out.println("house no is 65");
			break;
			
			case 24:
			System.out.println("house no is 24");
			break;
			
			case 48:
			System.out.println("house no is 48");
			break;
			
			default:
			System.out.println("not matched");
		}
	}
	}