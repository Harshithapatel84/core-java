class Bracelet {
	String brand;
	String material;
	String color;
	double weight;
	int price;
	String type;
	boolean adjustable;
	boolean waterproof;
	String design;
	double length;
	String gender;
	boolean handmade;
	String stoneType;
	int warrantyYears;
	String originCountry;
	boolean antique;
	String claspType;
	String occasion;
	int quantityAvailable;
	double discount;

     Bracelet(String brand, String material, String color, double weight, int price,
	         String type, boolean adjustable, boolean waterproof, String design,
	         double length, String gender, boolean handmade, String stoneType,
	         int warrantyYears, String originCountry, boolean antique,
	         String claspType, String occasion, int quantityAvailable, double discount) {

		this.brand = brand;
		this.material = material;
		this.color = color;
		this.weight = weight;
		this.price = price;
		this.type = type;
		this.adjustable = adjustable;
		this.waterproof = waterproof;
		this.design = design;
		this.length = length;
		this.gender = gender;
		this.handmade = handmade;
		this.stoneType = stoneType;
		this.warrantyYears = warrantyYears;
		this.originCountry = originCountry;
		this.antique = antique;
		this.claspType = claspType;
		this.occasion = occasion;
		this.quantityAvailable = quantityAvailable;
		this.discount = discount;
	}
}

