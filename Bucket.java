class Bucket{
	String color;
	double capacity;
	double price;
	String material;
	int noOfBucket;
	
	Bucket(String color,double capacity,double price,String material,int noOfBucket){
		this.color=color;
		this.capacity=capacity;
		this.price=price;
		this.material=material;
		this.noOfBucket=noOfBucket;
	}
}