class Alphabet{
	public static void main(String[] args)
	{
		char alphabet='B';
			switch (alphabet)
			{
				case 'A':
				System.out.println("A is a letter in alphabet");
				break;
				
				case 'B':
				System.out.println("B is a letter in alphabet");
				break;
				
				case 'C':
				System.out.println("C is a letter in alphabet");
				break;
				
				default:
                System.out.println("Invalid alphabet");
			}
		
	}
}