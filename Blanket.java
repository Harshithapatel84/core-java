class Blanket {
	String brand;
	String material;
	String color;
	double size;
	int price;
	boolean washable;

	Blanket(String brand, String material, String color, double size, int price, boolean washable) {
		this.brand = brand;
		this.material = material;
		this.color = color;
		this.size = size;
		this.price = price;
		this.washable = washable;
	}
}